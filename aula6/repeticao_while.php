<h2>Estrutura de repetição While</h2>
<hr>
<p>
    Utilizada para repetir um conjunto de instruções.
</p>
<strong>Exemplo</strong>
<pre>
    wihle(condição){
        //comandos a serem repetidos.
    }
</pre>

<?php
  $contador = 0;
  while(10){
        echo"Olá pessoal $contador<br>";
        $contador++;
    }
?>